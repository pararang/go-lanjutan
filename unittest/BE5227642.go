package unittest

func HomeworkBE5227642(input string) string {
	if input == "" {
		return "add more chars"
	}

	runes := []rune(input)
	if len(runes) > 20 {
		return "to much"
	}

	for i, j := 0, len(runes)-1; i < j; i, j = i+1, j-1 {
		runes[i], runes[j] = runes[j], runes[i]
	}

	return string(runes)
}
