package unittest

import (
	"strings"
	"unicode"
)

func HomeworkBE5180220(input string) map[string]int {
	if input == "" {
		return nil
	}

	if len(input) > 50 {
		return map[string]int{
			input: 1,
		}
	}

	letterCount := make(map[string]int)
	for _, letter := range input {
		if unicode.IsLetter(letter) {
			letterCount[strings.ToUpper(string(letter))]++
		}
	}
	return letterCount
}
