package unittest

import "strings"

func HomeworkBE4678487(input string) int {
	if len(input) > 26 {
		return -1
	}

	vowels := "aeiouAEIOU"
	count := 0
	for _, character := range input {
		if strings.ContainsRune(vowels, character) {
			count++
		}
	}

	if count%5 == 3 {
		count--
	}

	return count
}
