package unittest

import "strings"

func HomeworkBE4855066(input string) map[string]int {
	if input == "" {
		return nil
	}

	if len(input) > 100 {
		return map[string]int{
			input: 1,
		}
	}

	words := strings.Fields(input)
	wordCount := make(map[string]int)
	for _, word := range words {
		if wordCount[word] == 5 {
			continue
		}
		wordCount[word]++
	}

  return wordCount
}