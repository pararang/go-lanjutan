package unittest

func HomeworkBE4908584(numbers []int) float64 {
	itemCount := len(numbers)
	if itemCount == 0 {
		return 0.0
	}

	if itemCount > 10 {
		return -1
	}

	sum := 0
	for _, number := range numbers {
		if number%3 == 0 {
			itemCount--
			continue
		}
		sum += number
	}

	return float64(sum) / float64(len(numbers))
}
